function slugify(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '');
}

function buildBenefitType(code, label, description) {
  return { code, label, description };
}

function buildTrainingType(code, label, description) {
  return { code, label, description };
}

function buildProfileFlag(flagKey, label, category, questionPrompt, description) {
  return { flagKey, label, category, questionPrompt, description };
}

function buildProgram({
  programCode,
  programName,
  programFamily,
  programBucket,
  agency,
  federalDepartment,
  authority,
  assistanceListingNumber = '',
  targetPopulation,
  eligibilitySummary,
  benefitTypes = [],
  trainingTypes = [],
  administeredBy,
  officialSource,
  stateSource = '',
  applicationPath,
  relatedIndexes = [],
  relatedForms = [],
  deadlineRules = '',
  evidenceRequired = '',
  serviceScope = 'federal',
  priorityRank = 5,
  aliases = [],
  matchFlags = { required: [], preferred: [], excluded: [] }
}) {
  return {
    id: `workforce_program:${slugify(programCode)}`,
    programCode,
    programName,
    programFamily,
    programBucket,
    agency,
    federalDepartment,
    authority,
    assistanceListingNumber,
    targetPopulation,
    eligibilitySummary,
    benefitTypes,
    trainingTypes,
    administeredBy,
    officialSource,
    stateSource,
    applicationPath,
    relatedIndexes,
    relatedForms,
    deadlineRules,
    evidenceRequired,
    lastVerifiedAt: LAST_VERIFIED_AT,
    serviceScope,
    priorityRank,
    aliases,
    matchFlags: {
      required: matchFlags.required || [],
      preferred: matchFlags.preferred || [],
      excluded: matchFlags.excluded || []
    }
  };
}

function seedRows(db, sql, rows, mapper) {
  const statement = db.prepare(sql);
  rows.forEach((row) => {
    statement.run(mapper(row));
  });
  statement.finalize();
}

