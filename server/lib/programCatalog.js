function buildReferenceIndexRecord({ id, namespace, code, title, description, sourceName, officialSourceLink, authorityLevel, refreshFrequency, version, hierarchyLevel = 1 }) {
  return {
    id,
    namespace,
    code,
    title,
    description,
    parentCode: '',
    hierarchyLevel,
    effectiveDate: '',
    retiredDate: '',
    sourceName,
    officialSourceLink,
    lastCheckedAt: LAST_VERIFIED_AT,
    version,
    authorityLevel,
    refreshFrequency
  };
}

function buildReferenceCrosswalkRecord(sourceNamespace, sourceCode, targetNamespace, targetCode, relationshipType, sourceLink, confidence = 0.9) {
  return {
    id: `ref_crosswalk:${slugify([sourceNamespace, sourceCode, targetNamespace, targetCode, relationshipType].join(':'))}`,
    sourceNamespace,
    sourceCode,
    targetNamespace,
    targetCode,
    relationshipType,
    confidence,
    sourceLink,
    version: PROGRAM_CATALOG_VERSION,
    lastCheckedAt: LAST_VERIFIED_AT
  };
}

function expandProgramCrosswalks(program) {
  const rows = [];
  program.benefitTypes.forEach((benefitType) => {
    rows.push({
      id: `workforce_crosswalk:${slugify(`${program.programCode}:benefit:${benefitType}`)}`,
      programCode: program.programCode,
      relationType: 'provides_benefit',
      targetNamespace: 'BENEFIT_TYPE',
      targetCode: benefitType,
      notes: `${program.programName} can provide or route this benefit type.`,
      confidence: 0.95
    });
  });

  program.trainingTypes.forEach((trainingType) => {
    rows.push({
      id: `workforce_crosswalk:${slugify(`${program.programCode}:training:${trainingType}`)}`,
      programCode: program.programCode,
      relationType: 'supports_training_type',
      targetNamespace: 'TRAINING_TYPE',
      targetCode: trainingType,
      notes: `${program.programName} supports this training mode.`,
      confidence: 0.95
    });
  });

  ['required', 'preferred', 'excluded'].forEach((flagType) => {
    program.matchFlags[flagType].forEach((flagKey) => {
      rows.push({
        id: `workforce_crosswalk:${slugify(`${program.programCode}:${flagType}:${flagKey}`)}`,
        programCode: program.programCode,
        relationType: `${flagType}_profile_flag`,
        targetNamespace: 'PROFILE_FLAG',
        targetCode: flagKey,
        notes: `${program.programName} uses this profile flag in routing.`,
        confidence: flagType === 'preferred' ? 0.75 : 0.9
      });
    });
  });

  program.relatedIndexes.forEach((indexRef) => {
    const [targetNamespace, targetCode] = String(indexRef).split(':');
    if (!targetNamespace || !targetCode) {
      return;
    }

    rows.push({
      id: `workforce_crosswalk:${slugify(`${program.programCode}:index:${indexRef}`)}`,
      programCode: program.programCode,
      relationType: 'relates_to_reference_index',
      targetNamespace,
      targetCode,
      notes: `${program.programName} references this supporting index.`,
      confidence: 0.8
    });
  });

  program.relatedForms.forEach((formCode) => {
    rows.push({
      id: `workforce_crosswalk:${slugify(`${program.programCode}:form:${formCode}`)}`,
      programCode: program.programCode,
      relationType: 'may_use_form',
      targetNamespace: 'FORM',
      targetCode: formCode,
      notes: `${program.programName} may use this form in intake or dispute workflows.`,
      confidence: 0.8
    });
  });

  return rows;
}

